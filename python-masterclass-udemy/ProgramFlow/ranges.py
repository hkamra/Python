for i in range(1, 21, 2):   # the third parameter is a step, and forward stepping
    print("I is now {}".format(i))

for i in range(5):   # this will start from 0 by default
    print("I is now {}".format(i))

for i in range(10, 0, -2):   # this is backward stepping
    print("I is now {}".format(i))

# divisible by 7

for i in range(0, 100, 7):
    print(i)

